package automobili;

public class Marke {
	
	private int idMarke;
	private String imeMarke;
	private String zemlja;
	
	public int getIdMarke() {
		return idMarke;
	}
	public void setIdMarke(int idMarke) {
		this.idMarke = idMarke;
	}
	public String getImeMarke() {
		return imeMarke;
	}
	public void setImeMarke(String imeMarke) {
		this.imeMarke = imeMarke;
	}
	public String getZemlja() {
		return zemlja;
	}
	public void setZemlja(String zemlja) {
		this.zemlja = zemlja;
	}
	
	
}
